export function getRecommendedPastes(tdp: number): ThermalPaste[] {
    if (tdp === 0) return THERMAL_PASTES;
    return THERMAL_PASTES.filter(p => p.maxTdp >= tdp);
}

export function getInterval(task: MaintenanceTask, intensity: IntensityLevel): string {
    if (intensity === 'high') return task.highInterval;
    return task.baseInterval;
}

export function isTaskApplicable(task: MaintenanceTask, config: ConfigState): boolean {
    if (task.id === 'thermal_gpu' && !config.hasDiscreteGpu) return false;
    return true;
}

